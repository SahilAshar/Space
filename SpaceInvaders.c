void left_right_move (void){
	ADC_In89(ADCvalue);
	while((ADCvalue[0]<1500)&&(move>0)){
		move--;
		ST7735_DrawBitmap(move, 159, PlayerShip0, 18,8);
	}
	while(ADCvalue[0]>3500&&(move<109)){
		move++;
		ST7735_DrawBitmap(move, 159, PlayerShip0, 18,8);
	}
	while((ADCvalue[0]<3500)||(ADCvalue[0]>1500)){
		ST7735_DrawBitmap(move, 159, PlayerShip0, 18,8);
	}
}



